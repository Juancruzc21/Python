#Tipo de dato Entero o Largo

numero = 15
print(numero, type(numero))

#Tipo de dato Flotante
numeroFlotante = 15.5
print(numeroFlotante, type(numeroFlotante))

#Tipo de dato Complejo

numeroComplejo = 5 + 6j
print(numeroComplejo, type(numeroComplejo))

#Tipo de dato String
nombre = "Juan"
print(nombre, type(nombre))

#Tipo de dato Booleano
verdaderoFalso = 3 == 3
print(verdaderoFalso, type(verdaderoFalso))
